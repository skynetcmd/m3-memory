"""Generate a machine-readable tool-catalog manifest at docs/tools/MCP_CATALOG.json.

Imports `mcp_tool_catalog.TOOLS` (the single source of truth for every MCP
tool the bridge can register) and emits one compact record per tool:

  - name        — the catalog tool name
  - domain      — lazy-loading domain bucket (via tool_domains.domain_of_tool)
  - summary     — first sentence of the description, truncated to <=100 chars
  - destructive — True when the tool mutates/deletes (i.e. not default_allowed)
  - args        — [{name, type, required}] from parameters.properties, with the
                  universal "database" arg dropped (it's injected everywhere and
                  carries no per-tool signal)

Plus two top-level fields:

  - count          — number of non-meta tools (names not starting with "tools_").
                     This is the number the public docs quote ("N tools"); the
                     meta-tools tools_list_domains / tools_load_domain are the
                     lazy-loading escape hatch and are excluded by convention.
  - generated_note — reminder that the file is auto-generated.

Output is deterministic: tools are sorted by (domain, name), keys are sorted,
indent=2. Re-running with no catalog change produces a byte-identical file, so
a diff in MCP_CATALOG.json always reflects a real catalog change. The
`tests/test_tool_count_drift.py` regression test reads the `count` field and
asserts the hardcoded "N tools" claims in the docs still agree with it.

This manifest is derived only from the public TOOLS catalog — no benchmark,
database, or runtime data is read or written.
"""
from __future__ import annotations

import json
from pathlib import Path

BASE_DIR = Path(__file__).resolve().parent.parent
BIN_DIR = BASE_DIR / "bin"
OUT_PATH = BASE_DIR / "docs" / "tools" / "MCP_CATALOG.json"

# The universal --database / database arg is injected on essentially every
# tool and conveys nothing tool-specific; drop it from the per-tool args so
# the manifest highlights the real signature.
_UNIVERSAL_ARGS = {"database"}

# Meta-tools (the lazy-loading escape hatch) are excluded from the headline
# count the docs quote. Their names share this prefix.
_META_PREFIX = "tools_"


def _first_sentence(description: str, limit: int = 100) -> str:
    """First sentence of `description`, truncated to <= `limit` chars.

    Splits on the first period-space boundary; if none is found the whole
    (stripped) description is used. The result is hard-capped at `limit`
    characters with an ellipsis so summaries stay scannable.
    """
    text = " ".join((description or "").split())
    if not text:
        return ""
    dot = text.find(". ")
    if dot != -1:
        text = text[: dot + 1]
    if len(text) > limit:
        text = text[: limit - 1].rstrip() + "…"
    return text


def _tool_args(parameters: dict) -> list[dict]:
    """Extract [{name, type, required}] from a ToolSpec's parameters schema.

    Drops the universal `database` arg. `required` is read from the schema's
    top-level `required` list (JSON-Schema convention) so it reflects what the
    bridge actually enforces.
    """
    props = (parameters or {}).get("properties", {}) or {}
    required = set((parameters or {}).get("required", []) or [])
    out: list[dict] = []
    for name, spec in props.items():
        if name in _UNIVERSAL_ARGS:
            continue
        out.append({
            "name": name,
            "type": (spec or {}).get("type", "string"),
            "required": name in required,
        })
    return out


def build_manifest() -> dict:
    """Build the manifest dict from the live catalog.

    Imported lazily inside the function so the module stays importable even if
    bin/ isn't yet on sys.path at import time (the function adds it).
    """
    import sys
    if str(BIN_DIR) not in sys.path:
        sys.path.insert(0, str(BIN_DIR))
    import mcp_tool_catalog
    import tool_domains

    tools: list[dict] = []
    for spec in mcp_tool_catalog.TOOLS:
        tools.append({
            "name": spec.name,
            "domain": tool_domains.domain_of_tool(spec.name),
            "summary": _first_sentence(spec.description),
            "destructive": not spec.default_allowed,
            "args": _tool_args(spec.parameters),
        })
    tools.sort(key=lambda t: (t["domain"], t["name"]))

    count = len([t for t in tools if not t["name"].startswith(_META_PREFIX)])
    return {
        "count": count,
        "generated_note": (
            "Auto-generated by bin/gen_tool_manifest.py from "
            "mcp_tool_catalog.TOOLS — do not edit by hand. `count` excludes "
            "the tools_* meta-tools (the lazy-loading escape hatch); it is the "
            "number the public docs quote as 'N tools'. Re-run after changing "
            "the catalog."
        ),
        "tools": tools,
    }


def main() -> None:
    manifest = build_manifest()
    OUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    OUT_PATH.write_text(
        json.dumps(manifest, indent=2, sort_keys=True) + "\n",
        encoding="utf-8",
    )
    print(f"wrote {OUT_PATH.relative_to(BASE_DIR).as_posix()} "
          f"({manifest['count']} non-meta tools, "
          f"{len(manifest['tools'])} total)")


if __name__ == "__main__":
    main()
